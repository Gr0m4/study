package com.grom4.study.car;

public class Car {
    String brand;
    String model;
    int year ;
    String color;
    double price;
    String registration;

    public Car (String brand,
                String model,
                int year,
                String color,
                double price,
                String registration){
                            this.brand=brand;
                            this.model=model;
                            this.year=year;
                            this.color=color;
                            this.price=price;
                            this.registration=registration;
    }
}
